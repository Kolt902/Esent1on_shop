// Карта соответствия названий продуктов и специфичных изображений
const PRODUCT_NAME_TO_IMAGES_MAP: Record<string, string[]> = {
  "Nike Air Force 1": NIKE_AIR_FORCE_IMAGES,
  "Nike Air Jordan 1": NIKE_AIR_JORDAN_IMAGES,
  "Nike Dunk Low": NIKE_DUNK_IMAGES,
  "Adidas Stan Smith": ADIDAS_STAN_SMITH_IMAGES,
};

export const getImagesByProductName = (productName: string, index: number = 0): string[] => {
  // Ищем соответствие по имени продукта
  const nameKey = Object.keys(PRODUCT_NAME_TO_IMAGES_MAP).find(
    (key) => productName.toLowerCase().includes(key.toLowerCase())
  );

  if (nameKey) {
    return PRODUCT_NAME_TO_IMAGES_MAP[nameKey];
  }
  
  return [];
};

export const getImagesByCategory = (category: string, productId: number = 0, productName: string = ""): string => {
  // Сначала проверяем, есть ли у нас специфичные изображения для этого продукта
  const specificImages = getImagesByProductName(productName);
  if (specificImages.length > 0) {
    const imageIndex = productId % specificImages.length;
    return specificImages[imageIndex];
  }
  
  // Иначе выбираем по категории с дополнительным разнообразием, используя идентификатор продукта
  const categoryLower = category.toLowerCase();
  let images: string[] = [];
  
  if (categoryLower.includes('shoes') || categoryLower.includes('кроссовки') || categoryLower.includes('обувь') || categoryLower.includes('sneakers') || categoryLower.includes('running') || categoryLower.includes('basketball')) {
    images = SNEAKERS_IMAGES;
  } else if (categoryLower.includes('tshirt') || categoryLower.includes('футболк') || categoryLower.includes('t-shirt') || categoryLower.includes('футб')) {
    images = TSHIRTS_IMAGES;
  } else if (categoryLower.includes('hoodie') || categoryLower.includes('кофт') || categoryLower.includes('толстовк') || categoryLower.includes('jacket') || categoryLower.includes('outerwear')) {
    images = HOODIE_IMAGES;
  } else if (categoryLower.includes('pants') || categoryLower.includes('штан') || categoryLower.includes('брюки') || categoryLower.includes('джинс') || categoryLower.includes('jeans')) {
    images = PANTS_IMAGES;
  } else if (categoryLower.includes('access') || categoryLower.includes('аксессуар') || categoryLower.includes('bag') || categoryLower.includes('сумк') || categoryLower.includes('hat') || categoryLower.includes('шап')) {
    images = ACCESSORIES_IMAGES;
  } else {
    // Если категория не определена, используем изображения кроссовок по умолчанию
    images = SNEAKERS_IMAGES;
  }
  
  // Используем ID продукта для выбора конкретного изображения из массива
  // Добавляем небольшое смещение, чтобы продукты с одинаковой категорией имели разные картинки
  const imageIndex = (productId * 7 + 3) % images.length;
  return images[imageIndex];
};

